     */
    public function store(Request $request)
    {
        $this->validate($request,
        [
             'name' => 'required',
             'slug' => 'required',
        ]); 
        $tag = new Tag;
        $tag -> name = $request -> name;
        $tag -> slug = $request -> slug;
        $tag -> save();

        return redirect(route('admin.category.index'));
    }

